public class Calculadora {
    int suma(int a, int b) {
        return a + b;
    }
    
    int suma(int a, int b, int c) {
        return a + b + c;
    }
    
    double suma(double a, double b) {
        return a + b;
    }
    
    
    float suma(float a, float b) {
        return a + b;
    }
}
